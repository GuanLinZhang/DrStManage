public class ClinicBean {
    private String orgnazationNumber;
    private String name;
    private String address;
    private String state;
    private String date;
    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }




    public String getOrgnazationNumber() {
        return orgnazationNumber;
    }

    public void setOrgnazationNumber(String orgnazationNumber) {
        this.orgnazationNumber = orgnazationNumber;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }






}
